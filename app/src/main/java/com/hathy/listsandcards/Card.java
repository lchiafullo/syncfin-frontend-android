package com.hathy.listsandcards;

class Card {
    String name;
    String age;
    int photoId;

    Card(String name, String age) {
        this.name = name;
        this.age = age;
        this.photoId = photoId;
    }

    Card(int photoId) {
        this.name = name;
        this.age = age;
        this.photoId = photoId;
    }
}